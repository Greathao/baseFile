+(NSString *) md5: (NSString *) inPutText
{
    const char *cStr = [inPutText UTF8String];
    unsigned char result[CC_MD5_DIGEST_LENGTH];
    CC_MD5(cStr, strlen(cStr), result);
    
    return [[NSString stringWithFormat:@"%02X%02X%02X%02X%02X%02X%02X%02X%02X%02X%02X%02X%02X%02X%02X%02X",
             result[0], result[1], result[2], result[3],
             result[4], result[5], result[6], result[7],
             result[8], result[9], result[10], result[11],
             result[12], result[13], result[14], result[15]
             ] lowercaseString];
}
+(NSString *)SignAPi:(NSDictionary *)dic{
    //    排序
    NSArray *newArr = [dic.allKeys sortedArrayUsingSelector:@selector(caseInsensitiveCompare:)];
    NSMutableArray * ary = [NSMutableArray array];
    [newArr enumerateObjectsUsingBlock:^(id  _Nonnull ob, NSUInteger idx, BOOL * _Nonnull stop) {
        [dic enumerateKeysAndObjectsUsingBlock:^(id  _Nonnull key, id  _Nonnull obj, BOOL * _Nonnull stop) {
            
            if ([ob isEqualToString:key]) {
                
                if ([obj isKindOfClass:[NSString class]]||[obj isKindOfClass:[NSNumber class]]) {
                    if ([obj isKindOfClass:[NSNumber class]]) {
                        obj = [obj stringValue];
                    }
                    
                if (obj!=nil&&![obj isEqualToString:@""]&&![obj isEqualToString:@" "]) {
                        [ary addObject:[NSString stringWithFormat:@"%@=%@",ob,obj]];
                    }
                }
            } 
        }];
    }];
    // 拼接
    NSString * str = [[ary componentsJoinedByString:@"&"]uppercaseString];
    return [DesFunc md5:[NSString stringWithFormat:@"SING=HLGJSGK&%@",str]];
    
}


